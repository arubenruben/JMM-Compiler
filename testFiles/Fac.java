import shape;

class Rectange extends Shape {
    public static void main(String[] args) {
        int sum;
        int i;
        int[] A;
        A = new int[4];

        sum = 0;
        i = 0;

        while (i < A.length) {
            sum = sum + A[i];
            i = i + 1;
        }
        i = 0;
    }
}